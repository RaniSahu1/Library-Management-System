    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // save btn
        
        if(txtid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            txtid.requestFocus();
        }
       else if(txtname.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            txtname.requestFocus();
        }
       else if(txtpub.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            txtpub.requestFocus();
        }
       else if(txtprice.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            txtprice.requestFocus();
        }
       else if(txtyear.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            txtyear.requestFocus();
        }
        
        else {
            try {
                Connection con = connect.Connect();
                PreparedStatement p = con.prepareStatement("insert into book(id,name,publisher,price,year,status) values(?,?,?,?,?,?)");
                p.setString(1, txtid.getText());
                p.setString(2, txtname.getText());
                p.setString(3, txtpub.getText());
                p.setString(4, txtprice.getText());
                p.setString(5, txtyear.getText());
                p.setString(6, "NOT-ISSUED");
                p.executeUpdate();
                JOptionPane.showMessageDialog(this, "Record Saved");
                
                txtid.setText("");
                txtname.setText("");
                txtpub.setText("");
                txtprice.setText("");
                txtyear.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
    }//GEN-LAST:event_btnsaveActionPerformed

