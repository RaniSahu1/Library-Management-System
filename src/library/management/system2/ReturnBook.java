    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // search
        if(sid.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter student id and search");
            sid.requestFocus();
        }
        else {
            try {
                p = con.prepareStatement("SELECT s.name AS student_name,b.id AS book_id, b.name AS book_name, b.issueDate, b.dueDate FROM book AS b INNER JOIN student AS s ON b.studentId = s.id WHERE s.id = ?;");
               p.setString(1, sid.getText());
               r = p.executeQuery();
               
               if(r.next()){
                   bid.setText(r.getString(2));
                   sname.setText(r.getString(1));
                   duedate.setText(r.getString(5));
                   bname.setText(r.getString(3));
                   issuedate.setText(r.getString(4));
               }
               else{
                   JOptionPane.showMessageDialog(this, "please enter valid student id");
               }
            } catch (SQLException ex) {
                Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btnreturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreturnActionPerformed
        // return button
        
         if(sid.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter student id and search");
            sid.requestFocus();
        }
        else {
             try {
                 p = con.prepareStatement("update book set status=?, issueDate=?, dueDate=? , studentId=? where id=?");
                 p.setString(1, "ISSUED");
                 p.setString(2, "");
                 p.setString(3, "");
                 p.setNull(4, java.sql.Types.INTEGER);
                 p.setString(5, bid.getText());
                int rowsAffected = p.executeUpdate();
                if (rowsAffected > 0) {
                 JOptionPane.showMessageDialog(this, "Book returned Successfully!!");
                issuedate.setText("");
                duedate.setText("");
                bid.setText("");
                sid.setText("");
                bname.setText("");
                sname.setText("");
                } else {
        JOptionPane.showMessageDialog(this, "Book not found or could not be returned.");
    }
                
             } catch (SQLException ex) {
                 Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
             }
         }
    }//GEN-LAST:event_btnreturnActionPerformed

