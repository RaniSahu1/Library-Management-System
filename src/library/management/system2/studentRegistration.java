    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // save button
        if(txtid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All field are required");
            txtid.requestFocus();
        }
        else if(txtname.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All field are required");
            txtname.requestFocus();
        }
        else if(txtcourse.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All field are required");
            txtcourse.requestFocus();
        }
        
        else if(txtbranch.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All field are required");
            txtbranch.requestFocus();
        }
        else if(txtsem.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All field are required");
            txtsem.requestFocus();
        }
        
        else{
            try {
                Connection con = connect.Connect();
                PreparedStatement p = con.prepareStatement("insert into student (id, name, course, branch,semester) values(?,?,?,?,?)");
                p.setString(1, txtid.getText());
                p.setString(2, txtname.getText());
                p.setString(3, txtcourse.getText());
                p.setString(4, txtbranch.getText());
                p.setString(5, txtsem.getText());
                
                p.executeUpdate();
                JOptionPane.showMessageDialog(this, "Record Saved");
                txtid.setText("");
                txtname.setText("");
                txtcourse.setText("");
                txtbranch.setText("");
                txtsem.setText("");
                        } 
            catch (SQLException ex) {
                Logger.getLogger(studentRegistration.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

