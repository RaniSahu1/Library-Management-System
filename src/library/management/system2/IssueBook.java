    private void btnissueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnissueActionPerformed
        // save btn
        if(bid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            bid.requestFocus();
        }
        else if(sid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            sid.requestFocus();
        }
        else if(bname.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            bname.requestFocus();
        }
        else if(issuedate.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            issuedate.requestFocus();
        }
        else if(duedate.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are required");
            duedate.requestFocus();
        }
        else{
            try {
                p = con.prepareStatement("update book set status =?, issueDate=?, dueDate=?, studentId=?");
                p.setString(1, "ISSUED");
                p.setString(2, issuedate.getText());
                p.setString(3, duedate.getText());
                p.setString(4, sid.getText());
                p.executeUpdate();
                JOptionPane.showMessageDialog(this, "Book Issued");
                
                bid.setText("");
                    sid.setText("");
                        bname.setText("");
                            
                                duedate.setText("");
                
                        } catch (SQLException ex) {
                Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnissueActionPerformed

    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // search button
        if(bid.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Please enter the book id and search it");
        bid.requestFocus();
    }
        else{
            try {
                p = con.prepareStatement("select * from book where id = ?");
                p.setString(1,bid.getText());
                r = p.executeQuery();
                if(r.next()){
                    bname.setText(r.getString(2));
                }
            } catch (SQLException ex) {
                Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_btnsearchActionPerformed

